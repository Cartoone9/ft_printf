#include "ft_printf.h"

void	ft_dot(char **strr, int index, t_flags **flags)
{
	char	*dot;
	size_t	i;

	i = 1;
	dot = ft_strrchr(strr[index], '.');
	if (dot)
	{
		i = 1;
		(*flags)->dot = 1;
		(*flags)->preci = 0;
		while (dot[i] >= '0' && dot[i] <= '9')
			ft_preci_fill(flags, dot[i++]);
	}
}
