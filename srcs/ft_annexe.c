#include "ft_printf.h"

char	*ft_prefix(char c)
{
	if (c == 'X')
		return ("0X");
	return ("0x");
}

int	ft_upperhand(int str_len, int preci)
{
	if (str_len < preci)
		return (preci);
	return (str_len);
}
